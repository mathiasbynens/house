var ObjectID = mongo.ObjectID;
(exports = module.exports = function(house, options){
    
    // This endpoint requires a data source
    var ds = options.ds;
    var col = options.collection;
    var feedEndPoint = house.api.getEndPointByName("feed");
    
    var handleReq = function(req, res, next) {
        var path = req.hasOwnProperty('urlRouted') ? req.urlRouted : req.url;
        
        var insertDocToFeed = function(doc, callback) {
            var newFeedItem = {
                "ref": {"col": "todos", "id": doc.id},
                "todo": doc,
                "groups": doc.groups,
                "owner": doc.owner,
                "at": doc.at,
            }
            feedEndPoint({session: req.session, method: 'POST', url: '', fields: newFeedItem}, {end:function(){}, data:function(newFeedData){
                if(_.isArray(newFeedData)) {
                    newFeedData = _.first(newFeedData);
                }
                ds.update(col, {"_id": doc.id}, {"$set": {"feed": {id:newFeedData.id,at:newFeedData.at}}}, function(err, data) {
                    if(callback) {
                        callback(newFeedData);
                    }
                });
            },writeHead:function(){}});
        }
        var updateDocInFeed = function(doc) {
            var updateDoc = {
                "$set": {
                    "todo": doc,
                    "groups": doc.groups,
                    "owner": doc.owner,
                    "at": doc.at,
                }
            }
            feedEndPoint({session: req.session, method: 'PUT', url: '/'+doc.feed.id, fields: updateDoc}, {end:function(){}, data:function(newFeedData){
                if(_.isArray(newFeedData)) {
                    newFeedData = _.first(newFeedData);
                }
            },writeHead:function(){}});
        }
        var removeDocFromFeed = function(doc) {
            if(doc.feed && doc.feed.id) {
                feedEndPoint({session: req.session, method: 'DELETE', url: '/'+doc.feed.id, fields: {delete: true}}, {end:function(){}, data:function(newFeedData){
                },writeHead:function(){}});
            } else if(doc.id) {
                var feedQuery = {"ref": {"col": "todos", "id": doc.id}};
                ds.find('feed', feedQuery, function(err, data) {
                    _.each(data, function(e) {
                        var docId = e.id;
                        house.io.rooms.in('feed').emit('deletedFeed', docId);
                    });
                    ds.remove('feed', feedQuery, function(err, data) {
                    });
                });
            }
        }
        
        var countQuery = function(query) {
            if(query.hasOwnProperty('done')) {
                query.done = parseInt(query.done);
            }
            
            if(query.hasOwnProperty('list[id]')) {
                query['list.id'] = new ObjectID(query['list[id]']);
                delete query['list[id]'];
            } else if(query.hasOwnProperty('list.id')) {
                query['list.id'] = new ObjectID(query['list.id']);
            }
            
            if(query.hasOwnProperty('list[$exists]')) {
                if(query['list[$exists]'] == 'false') {
                    query['list'] = {"$exists": false};
                    delete query['list[$exists]'];
                }
            }
            
            // query mongo id's
            if(query.hasOwnProperty('id')) {
                query._id = new ObjectID(query.id);
                delete query.id;
            }
            if(!query.hasOwnProperty('$or')) {
                query["$or"] = [];
            }
            if(req.session.data.groups && req.session.data.groups.indexOf('admin') !== -1) {
            } else if(req.session.data.user) {
                query["$or"].push({"owner.id": req.session.data.user});
                if(req.session.data.groups) {
                    query["$or"].push({"groups": {$in: req.session.data.groups}});
                }
            } else {
                //query["groups"] = 'public';
                query["$or"].push({"groups": 'public'});
            }
            if(query["$or"].length == 0) {
                delete query["$or"];
            }
            ds.count(col, query, function(err, data){
                if(err) {
                    house.log.err(err);
                } else {
                    res.setHeader('X-Count', data);
                    res.data({});
                }
            });
        }
        var filterData = function(data) {
            /*if(!_.isArray(data)) {
                if(data.hasOwnProperty('updates')) {
                    _.each(data.updates, function(doc,ii){
                        delete data.updates[ii].src;
                    });
                }
            } else {
                _.each(data, function(doc, i){
                    if(doc.hasOwnProperty('updates')) {
                        _.each(doc.updates, function(doc,ii){
                            delete data[i].updates[ii].src;
                        });
                    }
                });
            }*/
            return data;
        }
        var findQuery = function(query) {
            if(query.id) {
                query._id = query.id;
                delete query.id;
            }
            if(query.hasOwnProperty('_id') && typeof query._id == 'string') {
                try {
                    query._id = new ObjectID(query._id);
                } catch(e) {
                    console.log('bad object id');
                }
            }
            
            if(query.hasOwnProperty('done')) {
                query.done = parseInt(query.done);
            }
            console.log(query);
            if(query.hasOwnProperty('list[id]')) {
                query['list.id'] = new ObjectID(query['list[id]']);
                delete query['list[id]'];
            }
            
            if(query.hasOwnProperty('list[$exists]')) {
                if(query['list[$exists]'] == 'false') {
                    query['list'] = {"$exists": false};
                    delete query['list[$exists]'];
                }
            }
            
            // query mongo id's
            if(query.hasOwnProperty('id')) {
                query._id = new ObjectID(query.id);
                delete query.id;
            }
            
            if(!query.hasOwnProperty('$or')) {
                query["$or"] = [];
            }
            if(req.session.data.groups && req.session.data.groups.indexOf('admin') !== -1) {
            } else if(req.session.data.user) {
                query["$or"].push({"owner.id": req.session.data.user});
                if(req.session.data.groups) {
                    var orGroups = req.session.data.groups;
                    orGroups.push('public');
                    query["$or"].push({"groups": {$in: orGroups}});
                } else {
                    query["$or"].push({"groups": 'public'});
                }
            } else {
                //query["groups"] = 'public';
                query["$or"].push({"groups": 'public'});
            }
            if(query["$or"].length == 0) {
                delete query["$or"];
            }
            ds.find(col, query, function(err, data){
                if(err) {
                    house.log.err(err);
                } else if(data) {
                    data = filterData(data);
                    res.data(data);
                } else {
                    house.log.err(new Error('no data from mongo'));
                }
            });
        }
        
        
        var docId;
        
        if(path.length > 1 && path.indexOf('/') === 0) {
            var docId = path.substr(1);
            docId = new ObjectID(docId);
        }
        
        if(req.method == 'GET') {
            var query = {};
            
            if(docId) {
                query._id = docId;
                findQuery(query);
            } else {
                if(req.query) {
                    query = req.query;
                    
                    // query mongo id's
                    if(query.hasOwnProperty('id')) {
                        query._id = new ObjectID(query.id);
                        delete query.id;
                    }
                }
                findQuery(query);
            }
        } else if(req.method == 'HEAD') {
            var query = {};
            
            if(docId) {
                query._id = docId;
                findQuery(query);
            } else {
                if(req.query) {
                    query = req.query;
                }
                countQuery(query);
            }
        } else if(req.method == 'POST') {
            house.log.debug('post');
            if(!req.session.data.user) {
                res.writeHead(403);
                res.end('{}');
                return;
            }
            if(path == '') {
                
                var newDoc = req.fields;
                
                if(newDoc.hasOwnProperty('list') && newDoc.list.id) {
                    newDoc.list.id = new ObjectID(newDoc.list.id);
                }
                
                if(newDoc.done) {
                    newDoc.done = parseInt(newDoc.done);
                }
                if(newDoc.rank) {
                    newDoc.rank = parseInt(newDoc.rank);
                }
                if(newDoc.dueAt) {
                    newDoc.dueAt = new Date(newDoc.dueAt);
                }
                  
                // can only insert to your own ownership
                newDoc.owner = {id: req.session.data.user, name: req.session.data.name};
                
                ds.insert(col, newDoc, function(err, data){
                    if(err) {
                        house.log.err(err);
                        res.end('error');
                    } else {
                        res.data(data);
                    }
                });
            }
        } else if(req.method == 'PUT') {
            if(!req.session.data.user) {
                res.writeHead(403);
                res.end('{}');
                return;
            }
            var query = {};
            if(req.session.data.hasOwnProperty('groups') && req.session.data.groups.indexOf('admin') !== -1) {
                
            } else {
                query['owner.id'] = req.session.data.user;
            }
            if(docId) {
                query._id = docId;
            
                var updateDoc = req.fields;
            
                if(updateDoc.hasOwnProperty('$set') && updateDoc["$set"]["done"]) {
                  updateDoc["$set"]["done"] = parseInt(updateDoc["$set"]["done"]);
                  if(updateDoc["$set"]["done"] === 1) {
                    updateDoc["$set"]["doneOn"] = new Date();
                    
                    // TODO if the todo was part of a list, indrement that lists doneCount
                    
                  } else {
                    updateDoc["$unset"]["doneOn"] = true;
                  }
                }
                if(updateDoc.hasOwnProperty('$set') && updateDoc["$set"]["list.id"]) {
                    updateDoc["$set"]["list.id"] = new ObjectID(updateDoc["$set"]["list.id"]);
                }
                if(updateDoc.hasOwnProperty('$set') && updateDoc["$set"]["list"] && updateDoc["$set"]["list"]["id"]) {
                    updateDoc["$set"]["list"]["id"] = new ObjectID(updateDoc["$set"]["list"]["id"]);
                }
                if(updateDoc.hasOwnProperty('$set') && updateDoc["$set"]["rank"]) {
                  updateDoc["$set"]["rank"] = parseInt(updateDoc["$set"]["rank"]);
                }
                if(updateDoc.hasOwnProperty('$set') && updateDoc["$set"].hasOwnProperty('dueAt')) {
                    updateDoc["$set"].dueAt = new Date(updateDoc["$set"].dueAt);
                }
                if(updateDoc.hasOwnProperty('$set') && updateDoc["$set"].hasOwnProperty('at')) {
                    updateDoc["$set"].at = new Date(updateDoc["$set"].at);
                }
                
                ds.update(col, query, updateDoc, function(err, data){
                    if(err) {
                        house.log.err(err);
                        res.end('error');
                    } else {
                        ds.find(col, query, function(err, data) {
                            var updatedDoc = _.first(data);
                            res.data(updatedDoc);
                        });
                    }
                });
            } else {
                res.writeHead(500);
                res.end('{}');
            }
        } else if(req.method == 'DELETE') {
            if(!req.session.data.user) {
                res.writeHead(403);
                res.end('{}');
                return;
            }
            var query = {};
            if(req.session.data.hasOwnProperty('groups') && req.session.data.groups.indexOf('admin') !== -1) {
                
            } else {
                query['owner.id'] = req.session.data.user;
            }
            if(docId) {
                query._id = docId;
                ds.remove(col, query, function(err, data){
                    if(err) {
                        house.log.err(err);
                        res.end('error');
                    } else {
                        res.data(data);
                    }
                });
                
            }
        } else if(req.method == 'OPTIONS') {
            
        }
    }
    return handleReq;
});
